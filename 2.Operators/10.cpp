// switch case
#include <bits/stdc++.h>
using namespace std;
int main()
{
    int value;
    cout << "enter the value" << endl;
    cin >> value;
    switch (value)
    {
    case 1:
        cout << "atharv";
        break;
    case 2:
        cout << "Vijay";
        break;
    case 3:
        cout << "Deshpande";
        break;
    default:
        cout << "started DSA";
        break;
    }
}