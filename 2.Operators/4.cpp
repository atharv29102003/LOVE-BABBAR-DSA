// post and pre increment operator
#include <bits/stdc++.h>
using namespace std;
int main()
{
    int a = 7;
    cout << a++ << endl; // pehele use karo fir increment karo
    // yaha pe print hone ke baad a ki value 8 ho gayi hai
    cout << ++a << endl; // pehele increment karo fir use karo
    // yaha par 8 mein 1 increment hua hai
}