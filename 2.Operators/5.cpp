// post and pre decrement operator
#include <bits/stdc++.h>
using namespace std;
int main()
{
    int a = 7;
    cout << a-- << endl; // pehele use karo fir decrement karo 
    // yaha pe print hone ke baad a ki value 6 ho gayi hai
    cout << a << endl;   // yaha par value 6 print ho rahi hai
    cout << --a << endl; // pehele decrement karo fir use karo
    // yaha par 6 mein 1 decrement hua hai
}